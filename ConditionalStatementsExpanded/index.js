let grade = 85;

if (grade >= 90) {
    console.log("Excellent! You got an A!")
} else if (grade >= 80) {
    console.log("Good job! You got B")
} else if (grade >= 70) {
    console.log("You passed with a C.")
} else if (grade >= 60) {
    console.log("You passed with a D.")
} else {
    console.log("You didn't pass this time. Keep trying!")
}